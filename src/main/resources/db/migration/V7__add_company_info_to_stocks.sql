-- ============================================================
-- ImiTrade — add extended company information to stocks.
--
-- Adds three nullable columns describing the issuing company:
--   * description — short company description (2–4 sentences)
--   * sector      — economic sector (<= 100 chars)
--   * website     — official company website (<= 255 chars)
--
-- Columns are intentionally nullable so the catalog remains open
-- for future instruments; every currently seeded ticker is then
-- backfilled with real public information about the company.
--
-- NOTE: a `logo_url` column is NOT added. The logo path is derived
-- on the backend from the ticker (file name in static/logos/).
-- ============================================================

ALTER TABLE stocks ADD COLUMN description TEXT;
ALTER TABLE stocks ADD COLUMN sector     VARCHAR(100);
ALTER TABLE stocks ADD COLUMN website    VARCHAR(255);

