/**
 * Точка мини-графика (sparkline) цены акции в каталоге.
 *
 * Источник — тот же эндпоинт `GET /api/v1/stocks/{ticker}/history`, что и у
 * большого графика на странице акции, но здесь нужен только `close` и только
 * для маленькой линии в строке таблицы. Локальный тип (а не импорт из
 * `features/stock-details`) — фичи не импортируют внутренности друг друга.
 */
export interface SparklinePoint {
  /** ISO-строка UTC-Instant, напр. `2025-01-01T00:00:00Z`. */
  time: string;
  /** Цена закрытия свечи. */
  close: number;
}
